  // Addition function
  function add(a, b) {
    return a + b;
  }
  
  // Subtraction function
  function subtract(a, b) {
    return a - b;
  }
  
  // Multiplication function
  function multiply(a, b) {
    return a * b;
  }
  
  // Division function
  function divide(a, b) {
    if (b === 0) {
      return "Error: Division by zero";
    }
    return a / b;
  }